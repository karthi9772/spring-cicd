package data;

public class Img {

	String name;
	int size;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSize() {
		return size;
	}	
	public void setSize(int size) {
		this.size = size;
	}
	public Img(String name, int size) {
		this.name = name;
		this.size = size;
	}
	public Img() {
	}
	
	
	
	
}
